const getAllDranks = () => {
  return dranks;
};

const getDrankTypes = (key) => {
  return dranks[key];
};

const getDrankRecipe = (id) => {
  const drankType = Object.keys(dranks).find((key) =>
    dranks[key].find((key) => key.id === id)
  );

  return dranks[drankType].find((drank) => drank.id === id);
};

const getDrank = (alchohol, strength) => {
  return dranks[`${alchohol}-${strength}`];
};

const getRandomDrank = () => {
  const typeArray = Object.keys(dranks);
  const randomType = typeArray[Math.floor(Math.random() * typeArray.length)];

  const randomDrank = Math.floor(Math.random() * dranks[randomType].length);

  return dranks[randomType][randomDrank];
};

